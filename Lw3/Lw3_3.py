
dict = {}

# TODO  Напишите функцию count_letters

def count_letters(str_arg: str):
    str_arg = str_arg.lower()
    str_arg = "".join(c  for c in str_arg if c.isalpha())
    for key in str_arg:
        if key not in dict:
            dict[key] = 1
        else:
            dict[key] +=1
    return dict

# TODO Напишите функцию calculate_frequency

def calculate_frequency(dict_arg: dict):
    S = dict_arg.values()
    Sum = sum(S)
    dict_arg = {k: round(v/Sum, 2) for k,v in dict_arg.items()}
    return dict_arg

